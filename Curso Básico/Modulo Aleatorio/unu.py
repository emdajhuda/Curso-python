import random
# Rango simple 0 <= r < 6
print (random.randrange(6)), (random.randrange(6))
# Rango mas complejo 1 <= r < 7
print (random.randrange(1,7)), (random.randrange(1,7))
# Rango realmente complejo de numeros pares entre 2 y 36
print (random.randrange(0,37,2))
# Numeros impares del 1 al 35
print(random.randrange(1,36,2))
